package net.emotivecloud.scheduler.onedrp;

public class DRPOneException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5430582062710886807L;

	public DRPOneException() {
		super();
	}

	public DRPOneException(String message, Throwable cause) {
		super(message, cause);
	}

	public DRPOneException(String message) {
		super(message);
	}

	public DRPOneException(Throwable cause) {
		super(cause);
	}
	
	
}
